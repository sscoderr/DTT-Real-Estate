class House {
  final int id;
  final int price;
  final String image;
  final String zip;
  final int bathrooms;
  final int bedrooms;
  final int size;
  final String city;
  final String description;
  final int latitude;
  final int longitude;
  final int distance;

  House({required this.id,
    required this.price,
    required this.image,
    required this.zip,
    required this.bathrooms,
    required this.bedrooms,
    required this.size,
    required this.city,
    required this.description,
    required this.latitude,
    required this.longitude,
    required this.distance,});
}